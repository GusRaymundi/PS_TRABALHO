; DEFINICAO DE MACROS

INCR  MACRO &X
        LDA &X
        ADD UMVAL
        STA &X
MEND


DECR MACRO &X
        LDA &X
        SUB UMVAL
        STA &X
MEND


INCR2 MACRO &Y
        INCR &Y
        INCR &Y
MEND


; PROGRAMA

TESTE START 0
        LDA VALOR
        STA RESULT
INCR RESULT
DECR RESULT
INCR2 RESULT
        LDA RESULT
        RSUB
UMVAL WORD 1
VALOR WORD 5
RESULT RESW 1
        END TESTE
